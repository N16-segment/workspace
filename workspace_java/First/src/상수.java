public class 상수 {
  public static void main(String[] args) {
    //상수 : 변하지 않는 값
    //변수는 값을 지속적으로 변경할 수 있음
    int num = 10;
    num = 20;

    //자료형 정수든 실수든 앞에 final 키워드를 추가하면 상수가 됨! 문자열도 가능 final String
    //상수는 값을 변경할 수 없음!
    //상수는 이름을 전부 다 대문자로 만든다!
    final int NUM2 = 10;
    //NUM2 = 20; 상수는 값 변경 시 오류 발생!


  }
}
